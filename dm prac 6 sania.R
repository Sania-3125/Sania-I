COS WAVE
x=seq(0,2*pi,0.01)
y=cos(x)
plot(x,y,type="l")


TAN WAVE
x=seq(0,2*pi,0.01)
y=tan(x)
plot(x,y,type="l")


ABSOLUTE WAVE
x=seq(-5,5,0.05)
y=abs(x)
plot(x,y,type="l")

CEILING WAVE
x=seq(-5,5,0.05)
y=ceiling(x)
plot(x,y,type="l")


FLOOR WAVE
x=seq(-5,5,0.05)
y=floor(x)
plot(x,y,type="l")


LOGARITHMIC WAVE
x=seq(10,100,5)
y=log(x)
plot(x,y,type="l")


EXPONENTIAL WAVE
x=seq(10,100,5)
y=exp(x)
plot(x,y,type="l")
